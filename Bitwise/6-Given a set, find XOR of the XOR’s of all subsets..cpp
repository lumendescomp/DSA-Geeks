/*
Time Complexity: O(1)
Auxiliary Space: O(1)
*/
// ---------------------CODE-----------------------
int findXOR(int Set[], int n)
{
    // XOR is 1 only when n is 1, else 0
    if (n == 1)
        return Set[0];
    else
        return 0;
}

